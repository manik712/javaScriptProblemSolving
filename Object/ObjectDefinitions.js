// In JavaScript, there are two main categories of data types: primitive and non-primitive (also known as reference types).



/*


Primitive Data Types: These are immutable data types that directly store their values in memory. There are six primitive data types in JavaScript:

Number: Represents numeric values, including integers and floating-point numbers.
String: Represents sequences of characters, enclosed within single quotes (''), double quotes ("") or backticks (``).
Boolean: Represents a logical value of true or false.
Null: Represents the intentional absence of any value.
Undefined: Represents the absence of a defined value.
Symbol (added in ECMAScript 6/ES2015): Represents unique identifiers, primarily used as object property keys.





Non-Primitive (Reference) Data Types: These are mutable data types that store references to their values. They are stored by reference in memory, meaning that when you create a variable of a non-primitive type, its value is stored as a reference to a location in memory where the actual value is stored. Non-primitive data types include:

Object: Represents a collection of key-value pairs. Objects can be created using object literals, constructors, or classes. Examples include arrays, functions, and objects created with the new keyword.
Array: A special type of object used for storing ordered collections of data.
Function: A type of object that can be invoked.
Date: Represents a specific moment in time.
RegExp (Regular Expression): Represents a pattern used in string searching and manipulation.
Primitive data types are passed by value, meaning that when you assign a primitive value to a variable or pass it as an argument to a function, a copy of the value is created. Non-primitive data types are passed by reference, meaning that when you assign a non-primitive value to a variable or pass it as an argument to a function, a reference to the original value in memory is passed.


*/



